package pack01;

public class Ex02 {
	public static void main(String[] args) {
		System.out.println(100); // 정수 상수 값
		System.out.println(1.0); // 실수 상수 값
		System.out.println("ABC"); // 문자열 상수 값
		System.out.println('A'); // 문자 상수 값
		System.out.println(true); // 불 상수 값
		System.out.println(false); // 불 상수 값

		System.out.println(10 + 20); // 불 상수 값
		System.out.println("10+20=" + 30); // 불 상수 값
		System.out.println("10+20=" + "의 결과는 30"); // 불 상수 값

		System.out.println(100);
		System.out.println("100");
		System.out.println("10" + 0);
		System.out.println(100 + ""); // ""엠티스트링
		System.out.println("" + 100);

		// 25 출력 +, ""이용
		System.out.println(2+5);
		System.out.println("2"+"5");
		System.out.println("25"+"");
		System.out.println(""+"25");
		System.out.println(2+"5");
		System.out.println("2"+5);
	}
}
